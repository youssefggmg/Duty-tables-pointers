#include <stdio.h>
#include <stdlib.h>
#include <time.h>

int main() {
    int Number[10], randomnum;
    int i, j, odd_count = 0;

    srand(time(NULL));
    for (i = 0; i < 10; i++) {
        randomnum = rand() % 100;
        Number[i] = randomnum;
    }
    printf("\n#################################################################################\n");
    printf("\n#################################################################################\n");
    printf("\t\tOriginal Numbers:\n");
    printf("\t\tIndex\tValue\n");;
    for (i = 0; i < 10; i++) {
        printf("\t\t%d\t%d\n", i, Number[i]);
    }
    printf("\n#################################################################################\n");
    printf("\n#################################################################################\n");
    for (i = 0; i < 10; i++) {
        if (Number[i] % 2 != 0) {
            Number[odd_count] = Number[i];
            odd_count++;
        }
    }
    printf("\n#################################################################################\n");
    printf("\n#################################################################################\n");
    printf("\t\tOdd Numbers:\n");
    printf("\t\tIndex\tValue\n");
    for (j = 0; j < odd_count; j++) {
        printf("\t\t%d\t%d\n", j, Number[j]);
    }
    printf("\n#################################################################################\n");
    printf("\n#################################################################################\n");
    return 0;
}

